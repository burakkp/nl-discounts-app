// YOUR EXTRACTED DEALS VIEW (Just the list and location header)
// ----------------------------------------------------------------------
class LocalDealsView extends ConsumerWidget {
  const LocalDealsView({super.key});

  @override
  Widget build(BuildContext context, WidgetRef ref) {
    final discountsAsyncValue = ref.watch(discountsProvider);
    final locationAsyncValue = ref.watch(locationProvider);

    // Notice there is no Scaffold here anymore, just the Column!
    return discountsAsyncValue.when(
      loading: () => const Center(child: CircularProgressIndicator()),
      error: (error, stackTrace) => Center(child: Text('🚨 Error: $error')),
      data: (deals) {
        return Column(
          children: [
            if (locationAsyncValue.value != null)
              Container(
                padding: const EdgeInsets.all(8.0),
                color: Colors.grey[200],
                width: double.infinity,
                child: Text('📍 Lat: ${locationAsyncValue.value!.latitude.toStringAsFixed(3)}, Lng: ${locationAsyncValue.value!.longitude.toStringAsFixed(3)}', textAlign: TextAlign.center),
              ),

            if (deals.isEmpty)
              const Expanded(child: Center(child: Text('No deals found within 15km.')))
            else
              Expanded(
                child: ListView.builder(
                  itemCount: deals.length,
                  itemBuilder: (context, index) {
                    final deal = deals[index];
                    return Card(
                      margin: const EdgeInsets.symmetric(horizontal: 16, vertical: 8),
                      child: ListTile(
                        leading: const Icon(Icons.local_offer, color: Colors.blue),
                        title: Text(deal['product'] ?? 'Unknown', style: const TextStyle(fontWeight: FontWeight.bold)),
                        subtitle: Text('${deal['supermarket']} • ${deal['distance_km']} km away'),
                        trailing: Text('€${deal['price']}', style: const TextStyle(fontSize: 18, fontWeight: FontWeight.bold, color: Colors.green)),
                      ),
                    );
                  },
                ),
              ),
          ],
        );
      }
    );
  }
}
